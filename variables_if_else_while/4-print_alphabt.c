#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
 * main - Entry point
 * Description: 'the program's description'
 * Return: Always 0 (Success)
 */

/* betty style doc for function main goes there */

int main(void)
{
	char n;

	n = 'a';
	while (n <= 'z')
	{
		if(n != ('q', 'e'));
		putchar(n);
		n++;
	}
	putchar('\n');
	return (0);
}
